import java.util.Scanner;

//4. Check if a number is divisible by both 3 and 5.
public class level2_01_04 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();

        for (int i = n; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
