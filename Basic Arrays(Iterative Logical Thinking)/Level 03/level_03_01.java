import java.util.HashSet;
import java.util.Scanner;
import java.util.Set;

public class level_03_01 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        // Approach 1: Brute Force
        int a = sc.nextInt();

        Set<Integer> set = new HashSet<>();

        while (a>0){
            int temp = a% 10;
            a = a/10;
            set.add(temp);
        }
        if(set.size() == 3){
            System.out.println("The number has 3 unique digits");
        }else{
            System.out.println("The Number do not have 3 unique digits");
        }

        //Approach 2 : optimal approach

//        int bit = 1 << digit; it means jaise ki har digit shuru kaha se hota hai 1 se na
        // but yaha pr ye kya kr raha hai har digit ke liye 1 assign kr raha hai but second position mein
        // means 1 == 2  and 2 == 4 sirf 1 daal raha hai na to meaning change ho jaa rha hai but usse digit
        // 1 side ho jaa rha hai.

//                | Digit | 1 << digit | Binary       |
//                | ----- | ---------- | ------------ |
//                | 1     | 2          | `0000000010` |
//                | 2     | 4          | `0000000100` |
//                | 3     | 8          | `0000001000` |

        int mask = 0;
        int aa = sc.nextInt();

        while (aa > 0){
            int digit = aa % 10;
            int bit = 1 << digit; // bit nikala

            if((mask & bit) != 0){
                System.out.println("Digits are duplicate");
                break;
            }
            mask |= bit; // yaha pr isme put kr diya taaki match kr ske
            aa = aa/10;
        }
    }
    // the internal working of this is that type like
}
